from dataclasses import dataclass


@dataclass(frozen=True)
class PromptSet:
    agent: str
    cron_agent: str
    compact: str


@dataclass(frozen=True)
class PromptProfile:
    system: str
    telegram: str
    cron: str
    cron_agent: str
    compact: str


